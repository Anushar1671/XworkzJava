package com.xworkz.app.java;

public class Thread {
	private String content;
	private String location;

	public Thread() {
	}

	public Thread(String content) {
		super();
		this.content = content;
	}

	public Thread(String content, String location) {
		super();
		this.content = content;
		this.location = location;
	}

}
