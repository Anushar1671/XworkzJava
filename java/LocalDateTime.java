package com.xworkz.app.java;

public class LocalDateTime {
	private int date;
	private int time;

	public LocalDateTime() {
	}

	public LocalDateTime(int date) {
		super();
		this.date = date;
	}

	public LocalDateTime(int date, int time) {
		super();
		this.date = date;
		this.time = time;
	}

}
