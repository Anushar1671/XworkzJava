package com.xworkz.app.java;

public class LocalDate {
	private int date;
	private int birthDate;

	public LocalDate() {
	}

	public LocalDate(int date) {
		super();
		this.date = date;
	}

	public LocalDate(int date, int birthDate) {
		super();
		this.date = date;
		this.birthDate = birthDate;
	}

}
